/** GameResult??playerStats??DB ???????PlayerGameStats[] ??????мҮ°л®ӣ????*/
function buildPlayerGameStatsRecords(
  game: import('../match/matchSimulator').GameResult,
  gameId: string,
  matchId: string,
  homeTeamId: string,
  awayTeamId: string,
): PlayerGameStats[] {
  const records: PlayerGameStats[] = [];
  let idx = 0;

  for (const stat of game.playerStatsHome) {
    records.push({
      id: `${gameId}_h${idx++}`,
      gameId,
      matchId,
      playerId: stat.playerId,
      teamId: homeTeamId,
      side: 'home',
      position: stat.position,
      kills: stat.kills,
      deaths: stat.deaths,
      assists: stat.assists,
      cs: stat.cs,
      goldEarned: stat.goldEarned,
      damageDealt: stat.damageDealt,
    });
  }

  idx = 0;
  for (const stat of game.playerStatsAway) {
    records.push({
      id: `${gameId}_a${idx++}`,
      gameId,
      matchId,
      playerId: stat.playerId,
      teamId: awayTeamId,
      side: 'away',
      position: stat.position,
      kills: stat.kills,
      deaths: stat.deaths,
      assists: stat.assists,
      cs: stat.cs,
      goldEarned: stat.goldEarned,
      damageDealt: stat.damageDealt,
    });
  }

  return records;
}

